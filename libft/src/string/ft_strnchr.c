int		ft_strnchr(const char *s, char ch, int n)
{
	int		res;

	if (ch == '\0' || !s)
		return (-1);
	res = 0;
	while (*s)
	{
		if (*s == ch)
			n--;
		if (n == 0)
			return (res);
		res++;
		s++;
	}
	return (-1);
}
